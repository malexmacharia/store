     */
    public function store(Request $request)
    {
        //
        $values=$request->validate([
            'name'=>'required|min:5',
            'phone'=>'required|regex:/(^(\+2547)[0-9]{8}$)/u',
            'address'=>'required',
            'email'=>'required'
        ]);
        Customer::create($values);
        return redirect()->route('customers.index')->with('success','Created Customer');
    }

